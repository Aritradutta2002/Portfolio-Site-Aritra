/* ── Animated proficiency ring (SVG arc) ─────────────────────── */
function ProficiencyRing({ level, color }: { level: number; color: string }) {
  const r = 22
  const circ = 2 * Math.PI * r
  const offset = circ - (level / 100) * circ

  return (
    <svg className="absolute inset-0 w-full h-full -rotate-90" viewBox="0 0 56 56">
      {/* Track */}
      <circle cx="28" cy="28" r={r} fill="none" stroke="rgba(255,255,255,0.06)" strokeWidth="3" />
      {/* Progress */}
      <motion.circle
        cx="28"
        cy="28"
        r={r}
        fill="none"
        stroke={color}
        strokeWidth="3"
        strokeLinecap="round"
        strokeDasharray={circ}
        initial={{ strokeDashoffset: circ }}
        whileInView={{ strokeDashoffset: offset }}
        transition={{ duration: 1.4, ease: 'easeOut', delay: 0.2 }}
        viewport={{ once: true }}
        style={{ filter: `drop-shadow(0 0 4px ${color})` }}
      />
    </svg>
  )
}

/* ── Single tech logo card ───────────────────────────────────── */
interface TechIconCardProps {
  name: string
  level?: number
  ringColor?: string
  size?: 'sm' | 'md' | 'lg'
  showName?: boolean
  floatDelay?: number
  animFloat?: boolean
}

export function TechIconCard({
  name,
  level,
  ringColor = '#8b5cf6',
  size = 'md',
  showName = true,
  floatDelay = 0,
  animFloat = false,
}: TechIconCardProps) {
  const sizeMap = { sm: 'w-12 h-12', md: 'w-16 h-16', lg: 'w-20 h-20' }
  const iconSizeMap = { sm: 'w-6 h-6', md: 'w-8 h-8', lg: 'w-10 h-10' }
  const logo = techLogos[name]

  return (
    <motion.div
      className="flex flex-col items-center gap-2 cursor-default"
      whileHover={{ y: -4, scale: 1.05 }}
      animate={animFloat ? { y: [0, -6, 0] } : undefined}
      transition={
        animFloat
          ? { duration: 3 + floatDelay * 0.5, repeat: Infinity, ease: 'easeInOut', delay: floatDelay }
          : { duration: 0.2 }
      }
    >
      {/* Icon container with proficiency ring */}
      <div className={`relative ${sizeMap[size]} flex items-center justify-center`}>
        {level !== undefined && <ProficiencyRing level={level} color={ringColor} />}
        {/* Glass card */}
        <motion.div
          className="w-[calc(100%-10px)] h-[calc(100%-10px)] rounded-xl glass-galaxy border border-white/10 flex items-center justify-center hover:border-white/25 transition-all duration-300 relative overflow-hidden group"
          whileHover={{ boxShadow: `0 0 20px ${ringColor}40, 0 0 40px ${ringColor}15` }}
        >
          {/* Subtle glow on hover */}
          <div
            className="absolute inset-0 opacity-0 group-hover:opacity-100 transition-opacity duration-300 rounded-xl"
            style={{ background: `radial-gradient(circle at center, ${ringColor}18 0%, transparent 70%)` }}
          />
          <div className={`${iconSizeMap[size]} relative z-10`}>
            {logo ?? (
              <span className="text-xs font-bold text-gray-400 text-center leading-tight">{name.slice(0, 3)}</span>
            )}
          </div>
        </motion.div>
      </div>

      {showName && (
        <span className="text-[10px] font-bold text-gray-400 text-center leading-tight max-w-[72px] truncate uppercase tracking-wide">
          {name}
        </span>
      )}
    </motion.div>
  )
}

